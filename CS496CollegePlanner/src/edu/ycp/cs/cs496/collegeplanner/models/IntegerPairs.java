package edu.ycp.cs.cs496.collegeplanner.models;

public class IntegerPairs {
	
	private int first;
	private int second;
	
	public IntegerPairs(int first, int second) {
		this.first = first;
		this.second = second;
	}
	
	public IntegerPairs() {
		
	}

	public int getFirst() {
		return first;
	}

	public void setFirst(int first) {
		this.first = first;
	}

	public int getSecond() {
		return second;
	}

	public void setSecond(int second) {
		this.second = second;
	}

}
